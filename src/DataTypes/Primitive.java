package DataTypes;

public class Primitive {
	public static void main(String args[]) {
		//taking integer values
		int i = 10;
		int b = 20;
		int addition = 10+20;
		int subraction = 10-20;
		int newInteger = 30;
		
		System.out.println(newInteger/i);
		System.out.println(newInteger/b);
		System.out.println("Add="+addition);
		System.out.println("Sub="+subraction);
		
		char oneCharacter = 'c';
		boolean aBoolean = true;
		float aFloat = 3.11f;
		
		System.out.println("char = "+oneCharacter);
		System.out.println("boolean="+aBoolean);
		System.out.println("float="+aFloat);
		
		

		
		
		
		
		
	}

}
